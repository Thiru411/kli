 <p>Term insurance Plan is a policy which provides pure protection to your family in the event of your death. It is the most basic type of life insurance plan that is available in the market. You are able to avail higher life coverage at an affordable premium and also add riders to maximize your coverage. Read more about term life insurance plans and its features <a href="https://insurance.kotak.com/insurance-guide/protection/what-is-term-insurance" title="">here</a>!</p>

