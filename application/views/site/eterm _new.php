 <p>A major advantage of investing in a term insurance policy is the tax benefit offered by the Income Tax Act, 1961. This Act offers numerous exemptions and deductions, thus reducing taxpayers’ liability where the insurance premiums can be claimed under Sections 80C and the death benefit can be availed as tax-free under Section 10(10D). Read more to know about the income tax deductions you can claim <a href="https://insurance.kotak.com/insurance-guide/savingstax/tax-benefit-of-investing-in-term-plan" title="">here</a>!</p>

 <h3 style="font-size:17px;font-weight: 500;"><b>3. How does term insurance policy work?</b></h3>

 <p>Term insurance operates on principles of pure insurance. While being simple and low-cost, it is a very effectual mode of insurance wherein the assured person is covered against the risks of mortality. The premium depends upon the sum assured and the age of the assured person. The younger the person, the lower the premium because the insurance company perceives the risk of mortality, i.e. death of a young and healthy person to be very less. Know more about how a term life insurance plan works by clicking <a href="https://insurance.kotak.com/insurance-guide/protection/how-does-term-life-insurance-work-learn-the-benefits" title="">here</a>.</p>

